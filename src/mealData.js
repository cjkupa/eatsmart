export function getMealSuggestion(cuisine, budget) {
  const key = cuisine ? cuisine.toLowerCase() : "default";
  const found = Object.keys(MEAL_DATA).find(k => key.includes(k));
  const tiers = MEAL_DATA[found || "default"];
  const match = tiers.find(t => budget <= t.budget) || tiers[tiers.length - 1];
  const canAfford = budget >= parseInt(match.typical.replace(/[^0-9]/,""));
  return { ...match, canAfford };
}
