export function getFeatured(name) {
  if (!name) return null;
  const key = Object.keys(FEATURED).find(k => name.toLowerCase().includes(k.toLowerCase()));
  return key ? FEATURED[key] : null;
}

export function getFeaturedForSearch(suburb, city) {
  return Object.entries(FEATURED)
    .filter(([_, v]) => v.city.toLowerCase() === city.toLowerCase())
    .map(([name, v]) => ({
      id: v.placeId || name,
      name,
      emoji: "🍽️",
      cuisine: v.cuisine || "restaurant",
      address: v.address || v.suburb + ", " + v.city,
      phone: v.phone,
      website: v.website,
      tags: [v.isOpen].filter(Boolean),
      isOpen: v.isOpen,
      rating: v.rating,
      ratingCount: v.ratingCount,
      priceLevel: null,
      isFeatured: true
    }));
}
